#  მეტობის ოპერატორი (>)
print(10 > 5)    # True
print(3 > 8)     # False
print(7 > 7)     # False
print(-1 > -2)   # True
print(0 > -5)    # True

#  ნაკლების ოპერატორი (<)
print(5 < 10)    # True
print(8 < 3)     # False
print(7 < 7)     # False
print(-2 < -1)   # True
print(-5 < 0)    # True

#  მეტობის ან ტოლობის ოპერატორი (>=)
print(10 >= 5)   # True
print(3 >= 8)    # False
print(7 >= 7)    # True
print(-1 >= -2)  # True
print(0 >= 0)    # True

#  ნაკლების ან ტოლობის ოპერატორი (<=)
print(5 <= 10)   # True
print(8 <= 3)    # False
print(7 <= 7)    # True
print(-2 <= -1)  # True
print(-5 <= 0)   # True

#  ტოლობის ოპერატორი (==)
print(5 == 5)    # True
print(8 == 3)    # False
print(7 == 7)    # True
print(-1 == -1)  # True
print(0 == 1)    # False

#  არ ტოლობის ოპერატორი (!=)
print(5 != 5)    # False
print(8 != 3)    # True
print(7 != 7)    # False
print(-1 != -2)  # True
print(0 != 1)    # True
